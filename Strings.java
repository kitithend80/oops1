public class Strings {
    public static void main(String[] args) {
        System.out.println("x: A.SAMHITHA,24011");

        String string = "Welcome! You are practicing strings concepts.";
        
        System.out.println("Capital letters: " + string.toUpperCase());
        System.out.println("Lower case: " + string.toLowerCase());
        System.out.println("Length: " + string.length());
        System.out.println("Index: " + string.indexOf(string));
    }
}
